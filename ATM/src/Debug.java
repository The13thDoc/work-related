/**
 * Static class to simplify certain common procedures.
 * 
 * @author Jon Henrich
 */
public class Debug {

	/**
	 * Print to the console. Executes <strong>System.out.<em>println</em>
	 * </strong>
	 * 
	 * @param str - String to print.
	 */
	public static void print(String str) {
		System.out.println(str);
	}
}
